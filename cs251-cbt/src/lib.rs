//! # CS 251: Cryptocurrencies and Blockchain Technologies
//!
//! Cryptocurrencies, blockchains, and smart contracts Fall 2022
//!
//! The course covers all aspects of blockchains and cryptocurrencies,
//! including distributed consensus, smart contracts, economics, scalability, and applications.
//! We will focus on Bitcoin and Ethereum as case studies.
//!
//! ## Course syllabus and readings
//!
//! - Lecture1: Intro to cryptography & cryptocurrencies
//!
//! ### Bitcoin mechanics
//! - Lecture2: Bitcoin nuts and bolts
//! - Lecture3: Wallets: managing and protecting crypto assets
//!
//! ### Consensus protocols
//!
//! - Lecture4: Classical consensus: network models and properties of secure state machine replication (SMR)  
//! - Lecture5: Consensus in the internet setting: dynamic availability, Nakamoto consensus, and sybil resistance
//! - Lecture6: Accountability and incentives in consensus: proof-of-stake
//!
//! ### Ethereum and decentralized applications
//!
//! - Lecture7: Ethereum: Decentralized Apps, EVM, and the Ethereum blockchain
//! - Lecture8: Programming in solidity
//!
//! ### Decentralized finance and economics
//!
//! - Lecture9: Stablecoins and oracles  
//! - Lecture10: Decentralized exchanges
//! - Lecture11: Lending systems
//! - Lecture12: Legal aspects and regulation
//!
//! ### Privacy on a public blockchain
//!
//! - Lecture13: Privacy: de-anonymizing the blockchain and mixing
//! - Lecture14: zk-SNARKs: what they are and how they are used
//! - Lecture15: Constructing a preprocessing zk-SNARK
//!
//! ### Scaling the blockchain
//!
//! - Lecture16: Scaling the blockchain: payment channels and state channels
//! - Lecture17: Scaling the blockchain: Optimistic Rollup, ZK-Rollup, and Validium
//! - Lecture18: Recursive SNARKs
//!
//! ### Final topics
//!
//! - Lecture19: Final topics: blockchain interoperability, maximal extractable value (MEV), and governance
//! - Lecture20: The future of blockchains
//!
//! ## Project
//! - Project #1: Merkle trees in Python
